package com.mavrick.common.auth;
/**
 * This enum represents the various Auth.states a userProfile(session) can be in . Based on the 
 * values present in session ACLs can be set .
 * 
 * @author oracle
 *
 */
public enum ProfileAuthStatus {
	
	
	ANONYMOUS("anonymous"),
	
	AUTO_LOGGED_IN("auto"),
	
	LOGIN_EXPLICIT("loggedin");
	
	
	private final String statusName;
	
	private ProfileAuthStatus(String name) {
		this.statusName=name;
	}

}
